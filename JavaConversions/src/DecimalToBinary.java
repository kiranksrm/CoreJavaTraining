
public class DecimalToBinary {

	public static void main(String[] args) {
		
		int i=5;
		System.out.println(Integer.toBinaryString(i));
		
		i=20;
		System.out.println(Integer.toBinaryString(i));
		System.out.println(Integer.toHexString(i));
		System.out.println(Integer.toOctalString(i));
		
	}

}
